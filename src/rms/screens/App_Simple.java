    private void initOrdersListModel(){
        
        listModel2 = new DefaultListModel();
    
        orders = Utils.arrangeOrders(ordersDb.getOrders());
        
        for(Order order : orders){
        
            listModel2.addElement("Order " + order.getId());
            
        }
        
        ordersList.setModel(listModel2);
    
    }
    
